public class FriendBean {

    private int ID;
    private String name;
    private String message;
    private String time;

    public int getID(){
        return ID;
    }

    public void setID(){
        this.ID = ID;
    }

    public String getName(){
        return name;
    }

    public void setName(){
        this.name = name;
    }

    public String getMessage(){
        return message;
    }

    public void setMessage(){
        this.message = message;
    }

    public String getTime(){
        return time;
    }

    public void setTime(){
        this.time = time;
    }
}
